      <h2>Préambule</h2>
      <p>
        Le présent règlement a pour but de définir :
        à    les principes de fonctionnement de l'ISTA Béni Mellal relevant de l’Office de la Formation Professionnelle et de la Promotion du Travail (OFPPT).
      </p>
      <p>
        à    les droits et les devoirs des stagiaires en formation initiale (diplômante et non diplômante), et des participants en formation continue (cours du soir, séminaires,…).
      </p>
      <p>
        Ce règlement doit être obligatoirement porté à la connaissance de l’ensemble du personnel et des stagiaires par tous les moyens jugés utiles par la Direction de l’EFP.
      </p>
      <h2>Admission en formation</h2>
      <p>
        1- L’admission dans les Établissements de Formation Professionnelle (EFP) des candidats répondant aux aptitudes requises se fait par voie des tests d’admission et/ou d’entretiens en fonction des places pédagogiques, et ce dans le cadre de la ‘’Sélection – Orientation ‘’ ou par toute autre voie instituée ou agréée par l’OFPPT, suivant les conditions d’accès prédéfinies.
      </p>
      <p>
        2- Sous réserve des dispositions du paragraphe 3 ci-dessous du présent chapitre, ne peuvent être admis aux différents EFP que les candidats de nationalité marocaine ou les étrangers résidents permanents au Maroc et répondant aux conditions d’accès fixées par la réglementation en vigueur.
      </p>
      <p>
        3- L’admission des candidats provenant de tout organisme conventionné avec l’OFPPT ainsi que celle des candidats étrangers ou résidants non permanents au Maroc se font conformément aux accords et conventions en vigueur.
      </p>
      <p>
        4- Le stagiaire handicapé admis bénéficiera d'une assistance particulière dans la mesure du possible. L'EFP prendra les mesures nécessaires pour faciliter son intégration dans la vie de l'EFP et sa participation effective à toutes les activités programmées.
      </p>
      <h2>Inscription</h2>
      <p>
        1- La constitution du dossier d’inscription est fixée par l’OFPPT.
      </p>
      <p>
        2- Le stagiaire doit s’acquitter de tous les frais exigés par l'OFPPT dont les montants sont fixés par la Direction Générale. Ils sont versés dans le compte bancaire de l'EFP ou remis à l’agent préposé de l’EFP.
      </p>
      <p>
        3- La direction de l'EFP délivre un reçu d'inscription au candidat qui a déposé son dossier ainsi que la preuve du règlement des droits d'inscription à la surveillance générale dans les délais fixés par la direction de l’EFP.
      </p>
      <p>
        4- La direction de l'EFP délivre au stagiaire une attestation d'inscription après avoir accompli toutes les formalités nécessaires.
      </p>
      <h2>Communication Interne</h2>
      <p>
        1- L'EFP doit être facilement repérable et reconnaissable par le public. De même qu'un système de signalisation est à installer au sein de l'EFP afin d'orienter aussi bien les usagers internes qu'externes.
      </p>
      <p>
        2- La charte graphique devra être respectée.
      </p>
      <p>
        3- Les panneaux d'affichage doivent être facilement accessibles et attractifs.
      </p>
      <h2>Passerelles</h2>
      <p>
        1- Un système de passerelles régi par une circulaire ministérielle est mis en oeuvre afin de permettre aux stagiaires les plus méritants de passer à des niveaux supérieurs.
      </p>
      <p>
        2- Le ou les bénéficiaires du système des passerelles, n'ayant pas le niveau scolaire requis pour la formation, doivent poursuivre des cours de mise à niveau, en dehors de l’horaire normal de leur formation, dispensés par l'EFP d'accueil.
      </p>
      <h2>Comportements</h2>
      <p>
        1- Les stagiaires sont tenus de :
        <br><br>- Respecter tout le personnel de l’ISTA;
        <br><br>- S'interdire de dégrader les biens mobiliers et immobiliers;
        <br><br>- Respecter la propreté de l'ISTA;
        <br><br>- S’abstenir de nuire à la bonne marche de la formation;
        <br><br>- Participer à l'entretien des locaux de formation et des espaces communs;
        <br><br>- Porter une tenue décente et être proprement vêtus et coiffés;
        <br><br>- Mettre la tenue de travail;
        <br><br>- S’abstenir de fumer conformément à la loi en vigueur;
        <br><br>- Maintenir le téléphone portable éteint dans les espaces de formation  ainsi que tout autre appareil de stockage et d’échange d’informations non autorisé;
        <br><br>- S’interdire l’utilisation de tout autre appareil susceptible de perturber le déroulement des activités de formation ou de troubler le fonctionnement normal de l’ISTA.
      </p>
